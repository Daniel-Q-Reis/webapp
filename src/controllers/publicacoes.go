//Entrelaçado com rotas/publicacoes.go

// CriarPublicacao chama a API para cadastrar uma publicação no banco de dados
func CriarPublicacao(w http.ResponseWriter, r *http.Request) {
	r.ParseForm()

	publicacao, erro := json.Marshal(map[string]string{ //json.Marshal retorna um slice of bytes para publicacao, precisa do Newbuffer abaixo para converter isso depois
		"titulo":   r.FormValue("titulo"),
		"conteudo": r.FormValue("conteudo"),
	})

	if erro != nil {
		respostas.JSON(w, http.StatusBadRequest, respostas.ErroAPI{Erro: erro.Error()})
		return
	}

	//vamos chamar a url da api para fazer a criação da publicação (vamos usar aquela rota)
	url := fmt.Sprintf("%s/publicacoes", config.APIURL) // vamos fazer a requisição com autenticacao abaixo
	response, erro := requisicoes.FazerRequisicaoComAutenticacao(r, http.MethodPost, url, bytes.NewBuffer(publicacao))
	if erro != nil {
		respostas.JSON(w, http.StatusInternalServerError, respostas.ErroAPI{Erro: erro.Error()})
		return
	}
	defer response.Body.Close()

	if response.StatusCode >= 400 {
		respostas.TratarStatusCodeDeErro(w, response)
		return
	}

	respostas.JSON(w, response.StatusCode, nil) //vamos devolver essa resposta ao front-end
}

// CurtirPublicacao chama a API para curtir uma publicação
func CurtirPublicacao(w http.ResponseWriter, r *http.Request) {
	//aqui não temos corpo na requisição, no js não estamos passando nenhuma informação, so estamos passando o ID por parametro pra essa rota
	//logo somente vamos ler o ID e chamar a API, passando esse ID
	parametros := mux.Vars(r) //com isso conseguimos ler os parametros
	publicacaoID, erro := strconv.ParseUint(parametros["publicacaoId"], 10, 64)
	if erro != nil {
		respostas.JSON(w, http.StatusBadRequest, respostas.ErroAPI{Erro: erro.Error()})
		return
	}

	//caso não der erro, vamos criar nossa URL, APIURL = URL da PAI, e depois o ID da publicação como placeholders
	url := fmt.Sprintf("%s/publicacoes/%d/curtir", config.APIURL, publicacaoID)
	response, erro := requisicoes.FazerRequisicaoComAutenticacao(r, http.MethodPost, url, nil)
	if erro != nil {
		respostas.JSON(w, http.StatusInternalServerError, respostas.ErroAPI{Erro: erro.Error()})
		return
	}
	defer response.Body.Close() //tem que dar o defer.body mesmo que a rota na API não retorne nenhum conteudo

	//tratando o statuscode de erro, ele também vai ficar disponivel em nosso JS publicacoes.js
	if response.StatusCode >= 400 {
		respostas.TratarStatusCodeDeErro(w, response)
		return
	}

	respostas.JSON(w, response.StatusCode, nil)
}

